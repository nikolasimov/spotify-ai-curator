import { NextRequest, NextResponse } from "next/server";
import { getSession } from "@/lib/session";
import {
  createPlaylist,
  addTracksToPlaylist,
  searchTrack,
} from "@/lib/spotify";

interface ExportBody {
  name?: string;
  mood?: string;
  recommendations: { name: string; artist: string }[];
}

export async function POST(req: NextRequest) {
  const session = await getSession();

  if (!session) {
    return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
  }

  try {
    const body: ExportBody = await req.json();
    const { recommendations, mood } = body;

    if (!recommendations?.length) {
      return NextResponse.json(
        { error: "No recommendations to export" },
        { status: 400 },
      );
    }

    // search Spotify for each recommended track
    const found: { uri: string; name: string; artist: string }[] = [];
    const notFound: string[] = [];

    for (const rec of recommendations) {
      const result = await searchTrack(
        session.accessToken,
        rec.name,
        rec.artist,
      );
      if (result) {
        found.push(result);
      } else {
        notFound.push(`${rec.name} — ${rec.artist}`);
      }
    }

    if (found.length === 0) {
      return NextResponse.json(
        {
          error:
            "Couldn't find any of those tracks on Spotify. The AI may have suggested songs that don't exist.",
        },
        { status: 404 },
      );
    }

    // generate a nice playlist name
    const playlistName =
      body.name ||
      generatePlaylistName(mood, found.length);

    const description = mood
      ? `AI Curator — "${mood.slice(0, 80)}"${mood.length > 80 ? "…" : ""}`
      : "Generated by Spotify AI Curator";

    // create the playlist and populate it
    const playlist = await createPlaylist(
      session.accessToken,
      session.user.id,
      playlistName,
      description,
    );

    const uris = found.map((t) => t.uri);
    await addTracksToPlaylist(session.accessToken, playlist.id, uris);

    return NextResponse.json({
      url: playlist.external_urls.spotify,
      name: playlistName,
      trackCount: found.length,
      notFound: notFound.length > 0 ? notFound : undefined,
    });
  } catch (err) {
    const message = err instanceof Error ? err.message : "export failed";
    console.error("export failed:", message);
    return NextResponse.json({ error: message }, { status: 500 });
  }
}

// ─── smart playlist naming ──────────────────────────────────────────────────

const TIME_LABELS: Record<string, string> = {
  morning: "Morning",
  afternoon: "Afternoon",
  evening: "Evening",
  night: "Late Night",
  midnight: "Midnight",
};

const MOOD_TITLES: [RegExp, string][] = [
  [/chill|relax|calm|peaceful|mellow/, "Chill Vibes"],
  [/sad|melanchol|lonely|heartbr|cry/, "In My Feelings"],
  [/hype|energy|pump|workout|gym|run/, "All Gas"],
  [/happy|joy|upbeat|cheerful|good mood/, "Good Energy"],
  [/focus|study|concentrate|work|code/, "Deep Focus"],
  [/driv|road|cruise|highway/, "Road Trip Mix"],
  [/nostalg|throwback|old school|retro/, "Nostalgia Trip"],
  [/party|dance|club|friday|weekend/, "Let's Go"],
  [/sleep|dream|ambient|drift/, "Drift Off"],
  [/rain|storm|cozy|autumn|winter/, "Rainy Day"],
  [/love|romantic|crush|date/, "Heartstrings"],
  [/angry|rage|frustrated|heavy/, "Burn It Down"],
];

function generatePlaylistName(mood?: string, trackCount?: number): string {
  if (!mood?.trim()) {
    return `AI Curator Picks · ${trackCount ?? 0} tracks`;
  }

  const lower = mood.toLowerCase();

  // check for time of day
  for (const [key, label] of Object.entries(TIME_LABELS)) {
    if (lower.includes(key)) {
      return `${label} · AI Curator`;
    }
  }

  // check for mood patterns
  for (const [pattern, title] of MOOD_TITLES) {
    if (pattern.test(lower)) {
      return title;
    }
  }

  // fallback: use first few words of the mood
  const words = mood.trim().split(/\s+/).slice(0, 5).join(" ");
  const capped = words.charAt(0).toUpperCase() + words.slice(1);
  return capped.length > 35 ? `${capped.slice(0, 33)}…` : capped;
}
